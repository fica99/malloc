#include "libft.h"

int			ft_pathcmp(char *p1, char *p2)
{
	while (*p1 && *p1 == *p2)
	{
		p1++;
		p2++;
		if (*p1 == '/' || *p2 == '/')
		{
			while (*p1 == '/')
				p1++;
			while (*p2 == '/')
				p2++;
			continue ;
		}
	}
	return (*p1 - *p2);
}
